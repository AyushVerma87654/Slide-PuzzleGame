export const cal11 = (a: (string | number)[]) => {
  for (let i = 0; i < a.length - 1; i++) {
    for (let j = i + 1; j < a.length; j++) {
      if (j % 11 == i) {
        fun(i, j);
      } else {
        for (let z = 0; z < 11; z++) {
          if (i >= z && j >= z && i <= z * 11 && j <= z * 11) {
            fun(i, j);
          }
        }
      }
    }
  }
};
