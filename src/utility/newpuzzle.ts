export const swap2 = (
  string: number,
  x: number,
  y: number,
  totalNumbers: (string | number)[],
  setTotalNumbers: (a: (string | number)[]) => void
) => {
  let i = 0;
  let array = [...totalNumbers];
  array[string] = array[string + ++i * y * x];
  array[string + i * y * x] = array[string + ++i * y * x];
  array[string + i * y * x] = "";
  setTotalNumbers(array);
}; //
